    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        
        if(exchange.size() <= 0){
            MessageBox.infoBox("Stocks are already empty", "Already Empty");
            return;
        }
        
        int reply = JOptionPane.showConfirmDialog(null, "Are you sure you want clear all stocks?", "Clear?", JOptionPane.YES_NO_OPTION);
        if (reply == JOptionPane.YES_OPTION) {
            exchange.clearStocks();
            managers.clear();
            lbxStockList.setModel(new DefaultListModel());
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        managers.clear();

        String[] stocks = {"INV", "ISPA", "VODA"};

        for (int i = 0; i < stocks.length; i++) {
            managers.add(new StockManager(stocks[i]));
        }

        DefaultListModel model = new DefaultListModel();

        for (StockManager m : managers) {
            model.addElement(m.getStockName());
        }

        lbxStockList.setModel(model);
    }//GEN-LAST:event_jButton3ActionPerformed

