    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        String stockName = jTextField1.getText().trim();
        
        if(exchange != null && exchange.getStocksManagers().size() > 5){
            MessageBox.infoBox("A Maximum of 5 stocks is allowed", "Stock Amount Exceeded");
            return;
        }
        
        if("".equals(stockName)){
            MessageBox.infoBox("Please enter a Stock name", "Empty field");
            return;
        }
        
        for(StockManager man : managers){
            if(man.getStockName().equals(stockName)){
                MessageBox.infoBox("Stock already exists", "Duplicate found");
                return;
            }
        }
        
        managers.add(new StockManager(stockName));
        
        DefaultListModel model = new DefaultListModel();
        
        for(StockManager man : managers){
            model.addElement(man.getStockName());
        }
        
        lbxStockList.setModel(model);
    }//GEN-LAST:event_jButton6ActionPerformed

