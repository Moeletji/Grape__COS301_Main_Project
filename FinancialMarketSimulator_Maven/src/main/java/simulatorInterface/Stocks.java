    public Stocks() {
        initComponents();
        Frame[] frames = getFrames();

        if (frames.length > 0) {
            frames[0].setResizable(false);
        }

        Cursor cursor = Cursor.getDefaultCursor();
        //change cursor appearance to HAND_CURSOR when the mouse pointed on images
        cursor = Cursor.getPredefinedCursor(Cursor.HAND_CURSOR);
        setCursor(cursor);

        DefaultListModel model = new DefaultListModel();

        Map<String, StockManager> man = exchange.getStocksManagers();

        if (man.size() > 0) {
            for (StockManager m : man.values()) {
                model.addElement(m.getStockName());
                managers.add(m);
            }

            lbxStockList.setModel(model);
        }

        lbxStockList.addKeyListener(new KeyListener() {
            @Override
            public void keyPressed(KeyEvent e) {
                if (e.getKeyCode() == KeyEvent.VK_DELETE) {
                    int index = lbxStockList.getSelectedIndex();
                    if (index >= 0 && index < managers.size()) {
                        StockManager mandelete = managers.remove(index);
                        exchange.removeStockManager(mandelete);

                        DefaultListModel model = new DefaultListModel();
                        if (exchange.getStocksManagers().size() > 0) {
                            for (StockManager m : exchange.getStocksManagers().values()) {
                                model.addElement(m.getStockName());
                            }
                            lbxStockList = new JList(model);
                        }
                    }
                }
            }

            @Override
            public void keyReleased(KeyEvent e) {
            }

            @Override
            public void keyTyped(KeyEvent e) {
            }
        });

    }

